int	ft_atoi(const char *str)
{
	short	n;
	int		res;
	int		i;

	i = 0;
	res = 0;
	n = 0;
	while (str[i] == '\f' || str[i] == '\t' || str[i] == '\r' || str[i] == '\v'
	|| str[i] == '\n' || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			n++;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		i++;
	}
	if (n == 1)
		res = -res;
	return (res);
}
