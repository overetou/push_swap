int	ft_strequ(char const *s1, char const *s2)
{
	unsigned int x;

	if (s1 && s2)
	{
		x = 0;
		while (s1[x] == s2[x] && s1[x] && s2[x])
			x++;
		return ((s1[x] - s2[x] != 0) ? 0 : 1);
	}
	return (0);
}
